# Wylicz wartości dziesiętne następujących liczb zapisanych w różnych
# systemach liczbowych:
# • 777 (ósemkowo)
# • 1011 (binarnie)
# • FFF (szesnastkowo)
# • 123 (piątkowo)

# • 777 (ósemkowo)
print(0o777)
print(7 * 8 ** 2 + 7 * 8 ** 1 + 7 * 8 ** 0)

# • 1011 (binarnie)
print(0b1011)
print(1 * 2 ** 3 + 0 * 2 ** 2 + 1 * 2 ** 1 + 1 * 2 ** 0)

# • FFF (szesnastkowo)
print(0xFFF)
print(15 * 16 ** 2 + 15 * 16 ** 1 + 15 * 16 ** 0)
